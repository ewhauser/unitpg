def run(
    cmd: list[str],
    *,
    env: dict[str, str],
    input_text: str | None = None,
    capture: bool = True,
    check: bool = True,
) -> subprocess.CompletedProcess[str]:
    return subprocess.run(
        cmd,
        env=env,
        input=input_text,
        text=True,
        stdout=subprocess.PIPE if capture else None,
        stderr=subprocess.PIPE if capture else None,
        check=check,
    )


class PsqlSession:
    def __init__(self, psql: str, conn_args: list[str], env: dict[str, str], label: str):
        self.label = label
        self.counter = 0
        self.proc = subprocess.Popen(
            [psql, *conn_args, "-d", "bench", "-X", "-qAt", "-v", "ON_ERROR_STOP=1"],
            env=env,
            stdin=subprocess.PIPE,
            stdout=subprocess.PIPE,
            stderr=subprocess.STDOUT,
            text=True,
            bufsize=1,
        )

    def execute(self, sql: str, label: str) -> str:
        if self.proc.stdin is None or self.proc.stdout is None:
            raise RuntimeError(f"{self.label} psql session is not open")
        if self.proc.poll() is not None:
            raise RuntimeError(f"{self.label} psql session exited before {label}")

        self.counter += 1
        sentinel = f"__fastfork_done_{self.counter}__"
        self.proc.stdin.write(sql)
        if not sql.endswith("\n"):
            self.proc.stdin.write("\n")
        self.proc.stdin.write(f"\\echo {sentinel}\n")
        self.proc.stdin.flush()

        output: list[str] = []
        while True:
            line = self.proc.stdout.readline()
            if line == "":
                if self.proc.poll() is not None:
                    raise RuntimeError(
                        f"{self.label} psql session exited during {label}:\n"
                        + "".join(output)
                    )
                continue
            if sentinel in line:
                return "".join(output)
            output.append(line)

    def close(self) -> None:
        if self.proc.poll() is None and self.proc.stdin is not None:
            try:
                self.proc.stdin.write("\\quit\n")
                self.proc.stdin.flush()
            except BrokenPipeError:
                pass
        try:
            self.proc.wait(timeout=5)
        except subprocess.TimeoutExpired:
            self.proc.terminate()
            try:
                self.proc.wait(timeout=5)
            except subprocess.TimeoutExpired:
                self.proc.kill()


def find_free_port() -> int:
    with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as sock:
        sock.bind(("127.0.0.1", 0))
        return int(sock.getsockname()[1])


def quote_sql_literal(value: str) -> str:
    return "'" + value.replace("'", "''") + "'"


def quote_conf_value(value: str) -> str:
    if value in {"on", "off"} or value.isdigit():
        return value
    return "'" + value.replace("'", "''") + "'"


def append_fast_config(data_dir: Path, socket_dir: Path, port: int) -> None:
    with (data_dir / "postgresql.conf").open("a", encoding="utf-8") as conf:
        conf.write("\n# bench/test_fastfork_epoch_rollback.py fast test settings\n")
        for key, value in FAST_SETTINGS.items():
            conf.write(f"{key} = {quote_conf_value(value)}\n")
        conf.write(f"unix_socket_directories = {quote_conf_value(str(socket_dir))}\n")
        conf.write(f"port = {port}\n")


def bin_path(bin_dir: Path, name: str) -> str:
    path = bin_dir / name
    if not path.exists():
        raise SystemExit(f"missing required PostgreSQL binary: {path}")
    return str(path)


def expect_error(
    psql: str,
    conn_args: list[str],
    env: dict[str, str],
    sql: str,
    expected: str,
) -> None:
    proc = run([psql, *conn_args, "-d", "bench"], env=env, input_text=sql, check=False)
    combined = (proc.stdout or "") + (proc.stderr or "")
    if proc.returncode == 0:
        raise RuntimeError(f"expected psql command to fail with {expected!r}")
    if expected not in combined:
        raise RuntimeError(f"expected error containing {expected!r}, got:\n{combined}")


def run_parallel_epoch(psql: str, conn_args: list[str], env: dict[str, str]) -> None:
    cmd = [psql, *conn_args, "-d", "bench", "-v", "ON_ERROR_STOP=1"]
    first = subprocess.Popen(
        [*cmd, "-c", PARALLEL_CONN1_SQL],
        env=env,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
    )
    try:
        time.sleep(0.2)
        second = run([*cmd, "-c", PARALLEL_CONN2_SQL], env=env, check=False)
        if second.returncode != 0:
            raise RuntimeError(
                "parallel epoch join failed:\n"
                f"{second.stdout or ''}{second.stderr or ''}"
            )

        stdout, stderr = first.communicate(timeout=10)
        if first.returncode != 0:
            raise RuntimeError(
                "parallel epoch leader failed:\n"
                f"{stdout or ''}{stderr or ''}"
            )
    finally:
        if first.poll() is None:
            first.terminate()
            try:
                first.wait(timeout=5)
            except subprocess.TimeoutExpired:
                first.kill()

    run([psql, *conn_args, "-d", "bench"], env=env, input_text=PARALLEL_VERIFY_SQL)


