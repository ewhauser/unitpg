def run_named_epoch_coordinator_connection(psql: str, conn_args: list[str], env: dict[str, str]) -> None:
    coordinator = PsqlSession(psql, conn_args, env, "named epoch coordinator")
    worker = PsqlSession(psql, conn_args, env, "named epoch worker")
    active = None

    try:
        coordinator.execute(CAPTURE_SQL, "capture coordinator fixture")

        for i in range(6):
            epoch = f"coordinator-reuse-{i}"
            coordinator.execute(
                f"SELECT pg_fastfork_epoch_start({quote_sql_literal(epoch)}, 'fixture');\n",
                f"start {epoch}",
            )
            worker.execute(
                f"""
SET ROLE postgres;
SELECT pg_fastfork_epoch_join({quote_sql_literal(epoch)});
RESET ROLE;
BEGIN;
INSERT INTO epoch_child VALUES ({600000 + i}, 2, 44);
ROLLBACK;
SELECT pg_fastfork_epoch_leave();
""",
                f"worker join/leave {epoch}",
            )
            fresh = run(
                [psql, *conn_args, "-d", "bench", "-v", "ON_ERROR_STOP=1", "-c", "SELECT 1;"],
                env=env,
                check=False,
            )
            if fresh.returncode != 0:
                raise RuntimeError(
                    "fresh connection failed while coordinator-owned epoch existed:\n"
                    f"{fresh.stdout or ''}{fresh.stderr or ''}"
                )
            coordinator.execute(
                f"""
SELECT pg_fastfork_epoch_finish({quote_sql_literal(epoch)});
SELECT pg_fastfork_epoch_finish({quote_sql_literal(epoch)});
""",
                f"finish {epoch}",
            )

        coordinator.execute(
            "SELECT pg_fastfork_epoch_start('coordinator-active-a', 'fixture');\n",
            "start coordinator-active-a",
        )
        active = subprocess.Popen(
            [
                psql,
                *conn_args,
                "-d",
                "bench",
                "-X",
                "-qAt",
                "-v",
                "ON_ERROR_STOP=1",
                "-c",
                """
SET ROLE postgres;
SELECT pg_fastfork_epoch_join('coordinator-active-a');
RESET ROLE;
BEGIN;
INSERT INTO epoch_child VALUES (600100, 2, 45);
SELECT pg_sleep(1.0);
ROLLBACK;
SELECT pg_fastfork_epoch_leave();
""",
            ],
            env=env,
            text=True,
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
        )
        time.sleep(0.2)

        fresh = run(
            [psql, *conn_args, "-d", "bench", "-v", "ON_ERROR_STOP=1", "-c", "SELECT 1;"],
            env=env,
            check=False,
        )
        if fresh.returncode != 0:
            raise RuntimeError(
                "fresh connection failed while another named epoch had an active participant:\n"
                f"{fresh.stdout or ''}{fresh.stderr or ''}"
            )

        coordinator.execute(
            "SELECT pg_fastfork_epoch_start('coordinator-active-b', 'fixture');\n",
            "start coordinator-active-b while coordinator-active-a is active",
        )
        worker.execute(
            """
SET ROLE postgres;
SELECT pg_fastfork_epoch_join('coordinator-active-b');
RESET ROLE;
BEGIN;
INSERT INTO epoch_child VALUES (600101, 3, 46);
COMMIT;
SELECT pg_fastfork_epoch_leave();
""",
            "worker join/leave coordinator-active-b",
        )
        coordinator.execute(
            "SELECT pg_fastfork_epoch_finish('coordinator-active-b');\n",
            "finish coordinator-active-b while coordinator-active-a is active",
        )

        stdout, stderr = active.communicate(timeout=10)
        if active.returncode != 0:
            raise RuntimeError(
                "active named epoch worker failed:\n"
                f"{stdout or ''}{stderr or ''}"
            )
        active = None

        coordinator.execute(
            "SELECT pg_fastfork_epoch_finish('coordinator-active-a');\n",
            "finish coordinator-active-a",
        )

        run(
            [psql, *conn_args, "-d", "bench"],
            env=env,
            input_text=r"""
\set ON_ERROR_STOP on
DO $$
BEGIN
  IF EXISTS (SELECT 1 FROM epoch_child WHERE id BETWEEN 600000 AND 600101) THEN
    RAISE EXCEPTION 'coordinator named epoch cleanup leaked inserted row';
  END IF;
END $$;
""",
        )
    finally:
        if active is not None and active.poll() is None:
            active.terminate()
            try:
                active.wait(timeout=5)
            except subprocess.TimeoutExpired:
                active.kill()
        worker.close()
        coordinator.close()


def run_named_session_visibility(psql: str, conn_args: list[str], env: dict[str, str]) -> None:
    run([psql, *conn_args, "-d", "bench"], env=env, input_text=CAPTURE_SQL + NAMED_SESSION_VISIBILITY_SETUP_SQL)

    cmd = [psql, *conn_args, "-d", "bench", "-v", "ON_ERROR_STOP=1"]
    reader = subprocess.Popen(
        [*cmd, "-c", NAMED_SESSION_VISIBILITY_READER_SQL],
        env=env,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
    )
    try:
        time.sleep(0.2)
        writer = run([*cmd, "-c", NAMED_SESSION_VISIBILITY_WRITER_SQL], env=env, check=False)
        if writer.returncode != 0:
            raise RuntimeError(
                "named epoch visibility writer failed:\n"
                f"{writer.stdout or ''}{writer.stderr or ''}"
            )

        stdout, stderr = reader.communicate(timeout=10)
        if reader.returncode != 0:
            raise RuntimeError(
                "named epoch visibility reader failed:\n"
                f"{stdout or ''}{stderr or ''}"
            )
    finally:
        if reader.poll() is None:
            reader.terminate()
            try:
                reader.wait(timeout=5)
            except subprocess.TimeoutExpired:
                reader.kill()

    run([psql, *conn_args, "-d", "bench"], env=env, input_text=NAMED_SESSION_VISIBILITY_FINISH_SQL)


def run_named_epoch_cleanup_after_error(psql: str, conn_args: list[str], env: dict[str, str]) -> None:
    setup_sql = (
        CAPTURE_SQL
        + "\nSELECT pg_fastfork_restore('fixture');\n"
        + "SELECT pg_fastfork_epoch_start('broken-cleanup', 'fixture');\n"
    )
    run([psql, *conn_args, "-d", "bench"], env=env, input_text=setup_sql)

    failed = run(
        [psql, *conn_args, "-d", "bench", "-v", "ON_ERROR_STOP=1", "-c", BROKEN_NAMED_CONN_SQL],
        env=env,
        check=False,
    )
    combined = (failed.stdout or "") + (failed.stderr or "")
    if failed.returncode == 0:
        raise RuntimeError("expected broken named epoch participant to fail")
    if "division by zero" not in combined:
        raise RuntimeError(
            "expected broken named epoch participant to fail with division by zero:\n"
            f"{combined}"
        )

    run([psql, *conn_args, "-d", "bench"], env=env, input_text=BROKEN_NAMED_RECOVERY_SQL)


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--bin", required=True, type=Path, help="PostgreSQL install bin directory")
    parser.add_argument("--keep-workdir", action="store_true", help="do not delete the disposable cluster")
    args = parser.parse_args()

    bin_dir = args.bin.resolve()
    initdb = bin_path(bin_dir, "initdb")
    pg_ctl = bin_path(bin_dir, "pg_ctl")
    createdb = bin_path(bin_dir, "createdb")
    psql = bin_path(bin_dir, "psql")

    env = os.environ.copy()
    env["PATH"] = f"{bin_dir}{os.pathsep}{env.get('PATH', '')}"
    env.setdefault("LC_ALL", "C")

    workdir = Path(tempfile.mkdtemp(prefix="fastfork-epoch-"))
    data_dir = workdir / "data"
    socket_dir = workdir / "socket"
    log_file = workdir / "postgres.log"
    socket_dir.mkdir(parents=True, exist_ok=True)
    port = find_free_port()
    started = False

    try:
        run([initdb, "-D", str(data_dir), "--no-sync", "-A", "trust", "-U", "postgres"], env=env)
        append_fast_config(data_dir, socket_dir, port)
        run([pg_ctl, "-D", str(data_dir), "-l", str(log_file), "-w", "start"], env=env)
        started = True

        conn_args = ["-h", str(socket_dir), "-p", str(port), "-U", "postgres"]
        run([createdb, *conn_args, "bench"], env=env)
        run([psql, *conn_args, "-d", "bench"], env=env, input_text=SETUP_SQL)
        run([psql, *conn_args, "-d", "bench"], env=env, input_text=CAPTURE_SQL + DML_SQL)
        run([psql, *conn_args, "-d", "bench"], env=env, input_text=CAPTURE_SQL + SESSION_SQL)
        run([psql, *conn_args, "-d", "bench"], env=env, input_text=CAPTURE_SQL + NAMED_SQL)
        run_parallel_named_epochs(psql, conn_args, env)
        run_parallel_named_finish_while_active(psql, conn_args, env)
        run_named_epoch_coordinator_connection(psql, conn_args, env)
        run_named_session_visibility(psql, conn_args, env)
        run_named_epoch_cleanup_after_error(psql, conn_args, env)
        run([psql, *conn_args, "-d", "bench"], env=env, input_text=CAPTURE_SQL + REUSED_BACKEND_NAMED_SQL)
        run([psql, *conn_args, "-d", "bench"], env=env, input_text=CAPTURE_SQL + "SELECT pg_fastfork_restore('fixture');\n")
        run_parallel_epoch(psql, conn_args, env)
        expect_error(
            psql,
            conn_args,
            env,
            CAPTURE_SQL + COMMIT_SQL,
            "fast-fork epoch transactions are rollback-only",
        )
        expect_error(
            psql,
            conn_args,
            env,
            CAPTURE_SQL + UNSUPPORTED_DDL_SQL,
            "DDL is not supported for this object type in fast-fork epoch transactions",
        )
        expect_error(
            psql,
            conn_args,
            env,
            CAPTURE_SQL + PREPARE_SQL,
            "prepared transactions are not supported in fast-fork epoch transactions",
        )
        run([psql, *conn_args, "-d", "bench"], env=env, input_text=CAPTURE_SQL + DDL_SQL)
        run([psql, *conn_args, "-d", "bench"], env=env, input_text=CAPTURE_SQL + DML_SQL)
        print("fast-fork epoch rollback smoke test passed")
        return 0
    except (RuntimeError, subprocess.CalledProcessError) as exc:
        if isinstance(exc, subprocess.CalledProcessError):
            if exc.stdout:
                print(exc.stdout, file=sys.stderr)
            if exc.stderr:
                print(exc.stderr, file=sys.stderr)
        else:
            print(str(exc), file=sys.stderr)
        print(f"cluster log: {log_file}", file=sys.stderr)
        return 1
    finally:
        if started:
            subprocess.run(
                [pg_ctl, "-D", str(data_dir), "-m", "fast", "-w", "stop"],
                env=env,
                text=True,
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
                check=False,
            )
        if args.keep_workdir:
            print(f"kept workdir: {workdir}", file=sys.stderr)
        else:
            shutil.rmtree(workdir, ignore_errors=True)


if __name__ == "__main__":
    raise SystemExit(main())
